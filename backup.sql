
CREATE TABLE lieux (
    id SERIAL PRIMARY KEY,
    categorie VARCHAR(255),
    latitude DECIMAL(10, 6),
    longitude DECIMAL(10, 6),
    ville VARCHAR(255),
    nom VARCHAR(255),
    photo VARCHAR(255)
);

CREATE TABLE lieux_files (
    id SERIAL PRIMARY KEY,
    lieux_id INT,
    directus_files_id UUID,
    FOREIGN KEY (lieux_id) REFERENCES lieux(id) ON DELETE CASCADE
);

